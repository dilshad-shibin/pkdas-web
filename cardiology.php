<h3 class="insub_title">Non-Invasive Cardiology </h3>  
<p>Advanced cardiac diagnostic procedures are carried out in the department of cardiology. These facilities complement the other world class facilities at the PK Das Institute of Medical Sciences. The diagnostic equipments include ECG machines, Treadmill machine, latest 2-D color Doppler Echocardiography machine etc.</p>
<p>The use of this full range of equipment and tests ensures that a complete picture emerges before preventive and curative measures are worked out.</p>
<p>The diagnostic tests which are routinely done include – Resting ECG, Stress ECG, transthoracic 2-D color echocardiography, tissue Doppler imaging in the department itself and carotid Doppler, peripheral Doppler (arterial and venous), renal Doppler etc. in the Radiology Department.</p>
</div>    
</div>    
    
<div class="Interventional_card text_justify">
<div class="container">
<h3 class="insub_title">Interventional Cardiology</h3>  
<p>Interventional cardiology involves treating patients with disease of the heart vessels or coronary artery disease and peripheral vessels. The interventional cardiology programme is supported by state-of-the-art Siemens Artis flatpanel catheterisation laboratory, equipped with features such as flat detectors to reduce radiation exposure to patients. The catheterisation laboratories are operational round-the-clock, ensuring that ongoing heart attack patients who admit into the hospital will receive acute angioplasty to clear up the blocked artery. This leads to improved clinical outcome and survival rates.</p>
<p>The Cardiology Department through its state-of-the-art Siemens Artis flat-panel catheterisation laboratory has performed several coronary and noncoronary procedures till date. In keeping with the cutting edge technology, the placement of drug eluting stents is a routine procedure here. For patients' comfort, we mostly adopt the trans-radial (through hand) approach for coronary interventions.</p>
<p>We are dedicated to the prevention, investigation and treatment of cardiac diseases.</p>
<p>It is our aim to work with our referring practitioners by meeting their individual requirements and providing their patients with the highest standard of care in a timely, compassionate and informed manner.</p>
