<h3 class="insub_title">Infrastructure</h3>
<p>Our unit has 2500sq.ft area (air-conditioned) with 10 ICU/ HDU beds, with both Premium & Economic paying wards. We are a well-equipped unit with 3modern ventilators, Non-invasive Ventilation units (CPAP & HFNC), Phototherapy lights, Multipara monitors including neonatal transport team &ambulance.</p>
<p>We have centralized oxygen;24-hour generator back up for all the equipments. We have blood gas analyser, centralized NABL-accredited laboratory, blood bank and a fully-stocked Pharmacy round the clock.
</p>
</div> 
<div class="neo_content_row">
<h3 class="insub_title">Human resources</h3>
<p>Our unit is run by a Neonatologist and 10 nurses. The specialistoffers 24x7 cover for NICU, high-risk deliveries and neonatal transport. In addition we also have specialists in the field of paediatricintensive care and other sub-specialties.</p>
</div>
<div class="neo_content_row">
<h3 class="insub_title">Services offered</h3>
<p>Neonatology department cares for babies less than 28 days of age and preterm babies born prior to 37 weeks of gestation. Our expertise is in care of high risk new-born, especially Very Low Birth Weight (babies below 1500g), surgical babies and babies with IEM. We are one of the largest neonatal care centres in Palakkad. We are well equipped to transport any sick neonates from other centres within a 60km radius.</p>
</div>
<div class="neo_content_row">
<h3 class="insub_title">Neonatal Screening services</h3>
<p>Our department emphasizes preventive aspect of new-born care as an inseparable and invaluable component of neonatology. We provide Immunization and universal screening programmes to detect hearing loss, hypothyroidism, congenital adrenal hyperplasia, critical congenital heart disease and metabolic disorders for the new-born babies.</p>
</div>
<div class="neo_content_row">
<h3 class="insub_title">Neonatal follow up services</h3>
<p>All new-born babies are followed up till 1 year  of age and care is then transferred to the Pediatrics Department. All high-risk preterm and term babies discharged from NICU get a comprehensive neurodevelopmental follow-up till 6 years of age, including developmental, nutritional, speech and language and ophthalmological assessments.</p>
</div>
<div class="neo_content_row">
<h3 class="insub_title">Community outreach services</h3>
<p>Doctors and nurses from our department are actively involved in door step vaccination and medical camp services in and around Palakkad.</p>

